// Characters needed to render every font name in the list
const PREVIEW_CHARS = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz 1234';

const BATCH_SIZE = 40;
const loadedBatches = new Set();

/**
 * Lazily load preview-subsetted Google Fonts CSS in batches.
 * Uses text= subsetting so only the chars needed for font names are downloaded.
 */
export function loadFontPreviews() {
    const batches = Math.ceil(GOOGLE_FONTS.length / BATCH_SIZE);
    for (let b = 0; b < batches; b++) {
        if (loadedBatches.has(b)) continue;
        loadedBatches.add(b);

        const slice = GOOGLE_FONTS.slice(b * BATCH_SIZE, (b + 1) * BATCH_SIZE);
        const families = slice
            .map((f) => `family=${f.name.replace(/ /g, '+')}:wght@400`)
            .join('&');
        const text = encodeURIComponent(PREVIEW_CHARS);
        const href = `https://fonts.googleapis.com/css2?${families}&text=${text}&display=swap`;

        const link = document.createElement('link');
        link.rel = 'stylesheet';
        link.href = href;
        link.setAttribute('data-seller-font-preview', String(b));
        document.head.appendChild(link);
    }
}
